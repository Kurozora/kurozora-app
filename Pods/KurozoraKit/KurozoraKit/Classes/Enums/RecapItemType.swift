public enum RecapItemType: String {
	// MARK: - Cases
	/// Indicates that the recap item is of the `shows` type.
	case shows

	/// Indicates that the recap item is of the `games` type.
	case games

	/// Indicates that the recap item is of the `literatures` type.
	case literatures

	/// Indicates that the recap item is of the `genres` type.
	case genres

	/// Indicates that the recap item is of the `themes` type.
	case themes
}
