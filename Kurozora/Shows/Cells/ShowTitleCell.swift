import UIKit

class ShowTitleCell: UITableViewCell {
	// MARK: - IBOutlets
	@IBOutlet weak var titleLabel: UILabel! {
		didSet {
			titleLabel.theme_textColor = KThemePicker.textColor.rawValue
			titleLabel.theme_backgroundColor = KThemePicker.backgroundColor.rawValue
		}
	}
	@IBOutlet weak var seeMoreButton: UIButton!
	@IBOutlet weak var separatorView: UIView! {
		didSet {
			separatorView.theme_backgroundColor = KThemePicker.separatorColor.rawValue
		}
	}

	// MARK: - Properties
	var titleText: String? {
		didSet {
			titleLabel.text = titleText
		}
	}
	var segueID: String?

	// MARK: - IBActions
	@IBAction func seeMoreButtonPressed(_ sender: UIButton) {
		guard let segueID = segueID else { return }
		parentViewController?.performSegue(withIdentifier: segueID, sender: nil)
	}
}
